-- Cricket Performance Analytics MySQL Project

-- 1. CREATE DATABASE
CREATE DATABASE IF NOT EXISTS CricketAnalytics;
USE CricketAnalytics;


-- 2. CREATE TABLES
CREATE TABLE Teams (
    team_id INT AUTO_INCREMENT PRIMARY KEY,
    team_name VARCHAR(50) NOT NULL,
    coach_name VARCHAR(50)
);

CREATE TABLE Players (
    player_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    age INT,
    role VARCHAR(20),
    team_id INT,
    FOREIGN KEY (team_id) REFERENCES Teams(team_id)
);

CREATE TABLE Tournaments (
    tournament_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    start_date DATE,
    end_date DATE
);

CREATE TABLE Matches (
    match_id INT AUTO_INCREMENT PRIMARY KEY,
    tournament_id INT,
    match_date DATE,
    team1_id INT,
    team2_id INT,
    location VARCHAR(50),
    FOREIGN KEY (tournament_id) REFERENCES Tournaments(tournament_id),
    FOREIGN KEY (team1_id) REFERENCES Teams(team_id),
    FOREIGN KEY (team2_id) REFERENCES Teams(team_id)
);

CREATE TABLE PlayerStats (
    stat_id INT AUTO_INCREMENT PRIMARY KEY,
    match_id INT,
    player_id INT,
    runs_scored INT DEFAULT 0,
    wickets_taken INT DEFAULT 0,
    catches INT DEFAULT 0,
    balls_faced INT DEFAULT 0,
    rating DECIMAL(3,1) DEFAULT 0,
    FOREIGN KEY (match_id) REFERENCES Matches(match_id),
    FOREIGN KEY (player_id) REFERENCES Players(player_id)
);

CREATE TABLE TeamStats (
    team_stat_id INT AUTO_INCREMENT PRIMARY KEY,
    match_id INT,
    team_id INT,
    runs_scored INT DEFAULT 0,
    wickets_lost INT DEFAULT 0,
    overs_played DECIMAL(4,1) DEFAULT 0,
    extras INT DEFAULT 0,
    FOREIGN KEY (match_id) REFERENCES Matches(match_id),
    FOREIGN KEY (team_id) REFERENCES Teams(team_id)
);


